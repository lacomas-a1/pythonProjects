import math
import cmath

math.log(5)
print(math.log(5))
print(math.log(5, math.e))
print(cmath.log(5))
print(math.log(1000, 10))
print(cmath.log(1000, 10))

# Special variations of the math.log function exist for different bases
print(math.log1p(5))
print(math.log2(8))
print(math.log10(100))
print(cmath.log10(100))