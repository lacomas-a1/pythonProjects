# Python Numpy allows you to perform arithmetic operations on an array using Arithmetic Operators.

import numpy as np
arr=np.array([10,15,25,35,45,55,65])
print(arr)

print(arr+4)
print(arr-8)
print(arr*3)

two=np.array([[1,2,3,4,5],[12,15,61,11,19]])
print(two)
print(two+10)
print(two-3)
print(two*2)

three=np.array([[1, 2, 3], [10, 20, 30], [6, 7, 8]])
print(three+2)
print(three-5)
print(three*5)